import numpy as np

first = np.array([1, 2, 3, 4, 5])
print(first)

first_modified = np.array(['1', 2, [3], 4, [5]])
print(first_modified)


second = np.array([[1, 1, 1],
                  [2, 2, 2]])
print(second)

second_modified = np.array([[1, 2, 3],
                            [4, 5]])
print(second_modified)
